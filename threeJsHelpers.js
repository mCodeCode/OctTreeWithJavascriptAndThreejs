const drawOctahedronWithLines = (octRadius, octColor) => {
  //----------------------------------------------------
  //----------------------------------------------------
  //----------------------------------------------------
  // DRAW Octahedron
  const octGeometry = new THREE.OctahedronGeometry(octRadius);
  const octMaterial = new THREE.MeshBasicMaterial({
    color: octColor,
  });
  const octahedron = new THREE.Mesh(octGeometry, octMaterial);

  //----------------------------------------------------
  //----------------------------------------------------
  //----------------------------------------------------
  // DRAW Octahedron WIREFRAME
  const wireOctFrameGeom = new THREE.OctahedronGeometry(octRadius);
  const WireOctEdges = new THREE.EdgesGeometry(wireOctFrameGeom);
  const wireOctLine = new THREE.LineSegments(WireOctEdges, new THREE.LineBasicMaterial({ color: colorList.black }));

  const meshHolder = {
    mesh: octahedron,
    wireframe: wireOctLine,
  };

  return meshHolder;
};
