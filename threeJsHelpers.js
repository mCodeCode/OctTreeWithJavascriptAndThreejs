const connectWithLine = (startPoint, endPoint, lineColor) => {
  let positionInfo = {
    posAX: startPoint.x,
    posAY: startPoint.y,
    posAZ: startPoint.z,
    posBX: endPoint.x,
    posBY: endPoint.y,
    posBZ: endPoint.z,
  };
  let tempLine = draw3DLine(positionInfo, lineColor);
  scene.add(tempLine);
};

