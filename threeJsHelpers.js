const draw3DLine = (positionInfo, lineColor) => {
  //create a blue LineBasicMaterial
  const material = new THREE.LineBasicMaterial({
    color: lineColor, //0x22a0ff
    linewidth: 200,
  });

  const points = [];
  // points.push(new THREE.Vector3(-10, 0, 0));
  // points.push(new THREE.Vector3(0, 10, 0));
  // points.push(new THREE.Vector3(10, 0, 0));
  points.push(new THREE.Vector3(positionInfo.posAX, positionInfo.posAY, positionInfo.posAZ));
  points.push(new THREE.Vector3(positionInfo.posBX, positionInfo.posBY, positionInfo.posBZ));

  const geometry = new THREE.BufferGeometry().setFromPoints(points);

  const line = new THREE.Line(geometry, material);

  return line;
};

