const drawCube = (_size, _color, _withWireframe = false) => {
  //----------------------------------------------------
  //----------------------------------------------------
  //----------------------------------------------------
  // DRAW cube
  const geometry = new THREE.BoxGeometry(_size, _size, _size);
  const material = new THREE.MeshBasicMaterial({
    color: _color,
  });
  const mesh = new THREE.Mesh(geometry, material);

  let wireframe = null;
  if (_withWireframe) {
    //----------------------------------------------------
    // DRAW WIREFRAME
    const wireGeom = new THREE.BoxGeometry(_size, _size, _size);
    const wireEdges = new THREE.EdgesGeometry(wireGeom);
    const wireframetmp = new THREE.LineSegments(wireEdges, new THREE.LineBasicMaterial({ color: colorList.black }));

    wireframe = wireframetmp;
  }

  const meshHolder = {
    mesh: mesh,
    wireframe: wireframe,
  };

  return meshHolder;
};
//----------------------------------------------------
//----------------------------------------------------
const drawBoxWireFrame = (_sizeW, _sizeH, _sizeD, _color) => {
  //----------------------------------------------------
  //----------------------------------------------------
  // DRAW WIREFRAME
  const wireGeom = new THREE.BoxGeometry(_sizeW, _sizeH, _sizeD);
  const wireEdges = new THREE.EdgesGeometry(wireGeom);
  const wireframetmp = new THREE.LineSegments(wireEdges, new THREE.LineBasicMaterial({ color: _color }));
  return wireframetmp;
};
