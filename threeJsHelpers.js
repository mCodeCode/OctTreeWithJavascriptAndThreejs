//camera control with WASD
document.addEventListener("keydown", (e) => {
  // console.log("QQQ  ", e);
  const cameraStep = 3;

  switch (e.key) {
    case "w":
      camera.position.set(camera.position.x, camera.position.y, camera.position.z - cameraStep);
      break;
    case "a":
      camera.position.set(camera.position.x - cameraStep, camera.position.y, camera.position.z);
      break;
    case "s":
      camera.position.set(camera.position.x, camera.position.y, camera.position.z + cameraStep);
      break;
    case "d":
      camera.position.set(camera.position.x + cameraStep, camera.position.y, camera.position.z);
      break;

    default:
      break;
  }
});

